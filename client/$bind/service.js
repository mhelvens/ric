'use strict';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
define(['app/module'], function (ApiNATOMY) {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	ApiNATOMY.factory('$bind', ['$rootScope', function ($rootScope) {
		return function $bind(fn) {

			return function () {

				var args = arguments;

				$rootScope.$apply(function () {

					fn.apply(undefined, args);

				});

			}

		};
	}]);


	return '$bind';


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
