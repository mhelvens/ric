'use strict';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
define(['lodash'], function (_) {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	//// Number division with one difference: Infinity / Infinity = 1

	_.mixin({
		div: function (a, b) {
			return (a === Infinity && b === Infinity) ? (1) : (a / b);
		}
	}, {chain: false});


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
