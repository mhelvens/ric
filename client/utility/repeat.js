'use strict';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
define(['lodash'], function (_) {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	_.mixin({
		repeat: function (value, count) {
			return _.times(count, function () { return value; });
		}
	}, {chain: true});


	_.mixin({
		repeatStr: function (value, count) {
			return new Array(count + 1).join(value);
		}
	}, {chain: false});


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
